// Programa para sacar promedio de 5 números
#include <iostream>
using namespace std;

int main() {
    float numero1 = 28;
    float numero2 = 32;
    float numero3 = 37;
    float numero4 = 24;
    float numero5 = 33;
    float suma = numero1 + numero2 + numero3 + numero4 + numero5;
    float promedio = suma / 5.0;
    cout << "El promedio es " << promedio << endl;
    return 0;
}
